from PIL import Image

# Bildgröße
width, height = 15, 8
img = Image.new("RGB", (width, height))

# Farbdefinitionen
colors_2 = {
    0: (255, 255, 255),  # weiß
    1: (128, 128, 128)   # grau
}

colors_7 = {
    '000': (255, 255, 255),  # weiß
    '001': (128, 128, 128),  # grau
    '010': (0, 0, 0),        # schwarz
    '011': (0, 0, 255),      # blau
    '100': (255, 0, 0),      # rot
    '101': (0, 255, 0),      # grün
    '110': (255, 255, 0)     # gelb
}

# Pixel-Daten (Zeilen x Spalten)
pixels = [
    [(0,0)]*15,
    [(0,0)]*2 + [(0,1)]*11 + [(0,0)]*2,
    [(1,'000'), (1,'001'), (1,'001'), (1,'010'), (1,'001'), (1,'001'), (1,'001'), (1,'011'), (1,'001'), (1,'001'), (1,'001'), (1,'001'), (1,'001'), (1,'001'), (1,'000')],
    [(1,'000'), (1,'001'), (1,'010'), (1,'001'), (1,'010'), (1,'001'), (1,'001'), (1,'001'), (1,'001'), (1,'001'), (1,'101'), (1,'001'), (1,'100'), (1,'001'), (1,'000')],
    [(1,'000'), (1,'001'), (1,'001'), (1,'010'), (1,'001'), (1,'010'), (1,'001'), (1,'010'), (1,'001'), (1,'001'), (1,'110'), (1,'001'), (1,'001'), (1,'001'), (1,'000')],
    [(0,0)] + [(0,1)]*13 + [(0,0)],
    [(0,0)]*2 + [(0,1)]*3 + [(0,0)]*5 + [(0,1)]*3 + [(0,0)]*2,
    [(0,0)]*15
]

# Pixel ins Bild schreiben
for y, row in enumerate(pixels):
    for x, (schema, val) in enumerate(row):
        if schema == 0:
            img.putpixel((x, y), colors_2[val])
        else:
            img.putpixel((x, y), colors_7[val])

# Bild speichern
img.save("bild.png")
img.show()
